//Destructuring
// Makes it easy to extract value from arrays or properties from objects

//Array
let fruits = ["Apple", "Mangoes"];
let [a, b] = fruits;
console.log(a);
console.log(b);

//Object
let person = { name: "Prath", age: "23" };
let { name, age } = person;

console.log(name);
console.log(age);
